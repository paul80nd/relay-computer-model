function toBytes(hex: string): number[] {
  const bytes: number[] = [];
  for (let i = 0; i < hex.length; i += 2) bytes.push(parseInt(hex.substr(i, 2), 16));
  return bytes;
}

test('calculates PI (full program runs to halt)', function () {
  const sut = new TestComputer();

  // Generous cap: the program halts after ~271,900 toggles; this guards CI
  // against an infinite loop should an opcode regress to a no-op.
  sut.runToHalt(toBytes(PI_PROGRAM_HEX), 5_000_000);

  // Halted at the `hlt` (0x03EC); pc points just past it.
  expect(sut.pcAddress).toBe(0x03ED);

  // 9-byte big-endian Q4.68 value of PI at label `pi` (0x0007).
  const pi = [0x32, 0x43, 0xf6, 0xa8, 0x88, 0x5a, 0x30, 0x8d, 0x31];
  const got = pi.map((_, i) => sut.memory(0x0007 + i));
  expect(got).toEqual(pi);
});
